
public class SwitchStatements {

	public static void main(String[] args) {
		
		
		int age = 30;
		
		//switch and case
		
		//Jumping Statements : break and continue;
		switch(age)
		{
		
		case 18 : System.out.println("Eligible for Vote - 18"); break;
		
		case 20 : System.out.println("Eligible for Vote-20"); break;
		
		case 22 : System.out.println("Eligible for Vote-22"); break;
		
		case 24 : System.out.println("Eligible for Vote-24"); break;
		
		case 25 : System.out.println("Eligible for Vote-25"); break;
		
		default : System.out.println("Not Eligible for Vote"); 
		
	}

}
}
