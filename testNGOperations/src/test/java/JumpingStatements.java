
public class JumpingStatements {

	public static void main(String[] args) {
		
		
		
		
		
		//Jumping Statements - break , continue
		
		// continue - will move to next loop
		
		//break - will move away from the loop
		
		for(int i =0;i<=5;i++)
		{
	
	if(i==3)
	{
	System.out.println("Print the value 3");break;
	}
	
	System.out.println("Print the value : " + i);
		}

}
}
