
public class LoopingStatements {
	
	// for Loop , While Loop and do While Loop
	
	//for Loop
	
	
	//for(intialisation ; condition;inc/dec)
	
	public static void main (String[]args) {
		//i = i+1
		
	for(int i=0;i<=5;i++)
	{
	
System.out.println("Print for loop block ");

	
	}
	
	//while 
	
	int j = 5;
	
	while(j<=4)
	{
	
		System.out.println("Print while loop block");
	
	j++;
	
}
	
	//do-while
	
	int k = 1;
	
	do
	{
		System.out.println("Print do while loop block");
		k++;	
	}
	while(k<=4);
	
	
	
	
	
	
	
	
	}
}
