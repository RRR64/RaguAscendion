package JavaClasses;

public class Encapsulation {

	
	private int a ;
	
	private int b =20 ;
	
	
	//this keyword represents that particular class
	
	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	
	
	//setters and getters

	//this - this class
	
}
